/**
 * P2-H6: 컴포넌트 자동 감사 생성기 (Component Audit Generator)
 *
 * src/components/*.vue 를 스캔하여 메타데이터(name/type/props/emits/dependencies)를
 * 추출하고, 타입 안전한 레지스트리 src/components/component_audit.ts 를 생성한다.
 *
 * 실행: npm run generate:audit  (node src/scripts/generate-component-audit.mjs)
 *
 * 파싱은 정규식 기반 best-effort 이며, 감사 레지스트리 용도로 충분한 근사 메타데이터를 제공한다.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const COMPONENTS_DIR = path.resolve(__dirname, '../components');
const OUTPUT_FILE = path.join(COMPONENTS_DIR, 'component_audit.ts');

/** 컴포넌트 이름 → 분류 (Page | Modal | Panel | Widget) */
function classify(name) {
  if (/(Dashboard|Monitor|Visualizer|Viewer)$/.test(name)) return 'Page';
  if (/Panel$/.test(name)) return 'Panel';
  if (/(Wizard|Center|Modal)$/.test(name)) return 'Modal';
  return 'Widget';
}

/** 객체/타입 본문에서 최상위(depth 0) 키만 추출 */
function topLevelKeys(body) {
  const keys = [];
  let depth = 0;
  let buf = '';
  for (const c of body) {
    if (c === '{' || c === '(' || c === '[' || c === '<') {
      depth++;
    } else if (c === '}' || c === ')' || c === ']' || c === '>') {
      depth = Math.max(0, depth - 1);
    } else if (depth === 0) {
      if (c === ':') {
        const token = buf.trim().replace(/[?]$/, '');
        const m = token.match(/([A-Za-z_$][\w$]*)$/);
        if (m) keys.push(m[1]);
        buf = '';
      } else if (c === ',' || c === ';' || c === '\n') {
        buf = '';
      } else {
        buf += c;
      }
    }
  }
  return [...new Set(keys)];
}

/** 배열 리터럴 본문에서 문자열 요소 추출 (defineEmits(['a','b']) 등) */
function stringArrayItems(body) {
  const items = [];
  const re = /['"]([^'"]+)['"]/g;
  let m;
  while ((m = re.exec(body)) !== null) items.push(m[1]);
  return [...new Set(items)];
}

function extractProps(src) {
  // defineProps({ ... })
  let m = src.match(/defineProps\s*\(\s*\{([\s\S]*?)\}\s*\)/);
  if (m) return topLevelKeys(m[1]);
  // defineProps<{ ... }>() — 인라인 타입
  m = src.match(/defineProps\s*<\s*\{([\s\S]*?)\}\s*>\s*\(/);
  if (m) return topLevelKeys(m[1]);
  // defineProps<TypeName>() — 명명된 interface/type 참조 (withDefaults 포함)
  m = src.match(/defineProps\s*<\s*([A-Za-z_$][\w$]*)\s*>\s*\(/);
  if (m) {
    const typeName = m[1];
    let t = src.match(new RegExp(`interface\\s+${typeName}\\s*\\{([\\s\\S]*?)\\n\\}`));
    if (!t) t = src.match(new RegExp(`type\\s+${typeName}\\s*=\\s*\\{([\\s\\S]*?)\\n\\}`));
    if (t) return topLevelKeys(t[1]);
  }
  // Options API: props: { ... }
  m = src.match(/\bprops\s*:\s*\{([\s\S]*?)\n\s{0,4}\}/);
  if (m) return topLevelKeys(m[1]);
  // Options API: props: [ ... ]
  m = src.match(/\bprops\s*:\s*\[([\s\S]*?)\]/);
  if (m) return stringArrayItems(m[1]);
  return [];
}

function extractEmits(src) {
  // defineEmits([ ... ])
  let m = src.match(/defineEmits\s*\(\s*\[([\s\S]*?)\]\s*\)/);
  if (m) return stringArrayItems(m[1]);
  // defineEmits<{ ... }>()
  m = src.match(/defineEmits\s*<\s*\{([\s\S]*?)\}\s*>\s*\(/);
  if (m) return topLevelKeys(m[1]);
  // Options API: emits: [ ... ]
  m = src.match(/\bemits\s*:\s*\[([\s\S]*?)\]/);
  if (m) return stringArrayItems(m[1]);
  return [];
}

function extractDependencies(src) {
  const deps = [];
  const re = /import\s+(?:[\s\S]*?)\s+from\s+['"]([^'"]+)['"]/g;
  let m;
  while ((m = re.exec(src)) !== null) {
    const source = m[1];
    if (source === 'vue') continue; // 프레임워크 자체는 제외
    deps.push(source);
  }
  return [...new Set(deps)];
}

function main() {
  const files = fs
    .readdirSync(COMPONENTS_DIR)
    .filter((f) => f.endsWith('.vue'))
    .sort();

  const entries = files.map((file) => {
    const name = file.replace(/\.vue$/, '');
    const src = fs.readFileSync(path.join(COMPONENTS_DIR, file), 'utf-8');
    return {
      name,
      path: `./${file}`,
      type: classify(name),
      props: extractProps(src),
      emits: extractEmits(src),
      dependencies: extractDependencies(src),
    };
  });

  const body = entries
    .map(
      (e) =>
        `  {\n` +
        `    name: ${JSON.stringify(e.name)},\n` +
        `    path: ${JSON.stringify(e.path)},\n` +
        `    type: ${JSON.stringify(e.type)},\n` +
        `    props: ${JSON.stringify(e.props)},\n` +
        `    emits: ${JSON.stringify(e.emits)},\n` +
        `    dependencies: ${JSON.stringify(e.dependencies)},\n` +
        `  },`
    )
    .join('\n');

  const content = `/**
 * AUTO-GENERATED by src/scripts/generate-component-audit.mjs — 수동 편집 금지.
 * 재생성: npm run generate:audit
 *
 * P2-H6 컴포넌트 감사 레지스트리: ${entries.length}개 컴포넌트.
 */

export type ComponentType = 'Page' | 'Modal' | 'Panel' | 'Widget';

export interface ComponentAuditEntry {
  name: string;
  path: string;
  type: ComponentType;
  props: string[];
  emits: string[];
  dependencies: string[];
}

export const COMPONENT_AUDIT: ComponentAuditEntry[] = [
${body}
];

export const COMPONENT_AUDIT_COUNT = ${entries.length};

// 시각(時刻)에 존재하고, 시간(時間)에 소멸한다.
// Exists in the Moment, Vanishes in Time.
`;

  fs.writeFileSync(OUTPUT_FILE, content, 'utf-8');
  console.log(`✅ component_audit.ts 생성 완료: ${entries.length}개 컴포넌트`);
  for (const e of entries) {
    console.log(`   - ${e.name} [${e.type}] props:${e.props.length} emits:${e.emits.length} deps:${e.dependencies.length}`);
  }
}

main();

